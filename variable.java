//variable are just like container to store data and information
//datatypes in java are int, float, double, char, string, boolean

//for example if I want represesnt  my information in java then I will use variable to store my information

//for example
//name-vikas ----> string
//age-23 ----> int
//height-5.8 ----> float
//weight-74.5 ----> float
//isStudent - true ----> boolean

public class variable {
    public static void main(String[] args) {
        String name = "vikas";
        int age = 23;
        float height = 5.8f;
        float weight = 74.5f;
        boolean isStudent = true;

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Weight: " + weight);
        System.out.println("Is Student: " + isStudent);
    }
}
